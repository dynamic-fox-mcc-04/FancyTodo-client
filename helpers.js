function showLogin() {
    $('.lds-dual-ring').hide()

    $('.login-page').show()
    $('.main-page').hide()
    $('.todos-page').hide()
    $('.invitation-page').hide()
    $('.showmembers-page').hide()
    $('.update-page').hide()

}


function showMain() {
    $('.lds-dual-ring').hide()

    $('.login-page').hide()
    $('.main-page').show()
    $('.todos-page').hide()
    $('.invitation-page').hide()
    $('.showmembers-page').hide()
    $('.update-page').hide()

}


